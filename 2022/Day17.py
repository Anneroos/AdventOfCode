def checkSidewaysSimple(shp):
    result = True
    xvalues = [pt[0] for pt in shp]
    if min(xvalues)<0 or max(xvalues)>6:
        result = False
    return result

@lru_cache(maxsize=None)
def moveSideways4Times(dirs, shp):
    shape = shp
    for direction in dirs:
        if direction == ">":
            newshape = moveShape(shape, 1, 0)
        else:
            newshape = moveShape(shape, -1, 0)
        check = checkSidewaysSimple(newshape)
        if check:
            shape = newshape
    return list(shape)



def moveShape(shp, dx, dy):
    # print(shp, dx, dy)
    return [(pt[0] + dx, pt[1] + dy) for pt in shp]

class tetrisGame():
    def __init__(self):
        self.directionIdx = 0
        self.grid = {}
        self.height = 0
        lineH = [(0, 0), (1, 0), (2, 0), (3, 0)]
        cross = [(1, 0), (0, 1), (1, 1), (2, 1), (1, 2)]
        weirdL = [(0, 0), (1, 0), (2, 0), (2, 1), (2, 2)]
        lineV = [(0, 0), (0, 1), (0, 2), (0, 3)]
        square = [(0, 0), (1, 0), (0, 1), (1, 1)]
        self.shapes = [lineH, cross, weirdL, lineV, square]
        self.shapeIdx = -1
        self.switch = 1

    def checkSidewaysPush(self, shp):
        result = True
        for pt in shp:
            # print("sidways check",pt, pt in grd, pt[0] < 0 , pt[0] > 6 , pt[1] < 1)
            if self.grid.get(pt, ".") == "#" or pt[0] < 0 or pt[0] > 6:
                result = False
                break
        return result

    def checkDownwards(self, shp):
        result = True
        for pt in shp:
            # print("downwards check", pt, pt in grd, pt[0] < 0 , pt[0] > 6 , pt[1] < 1)
            if self.grid.get(pt, ".") == "#" or pt[1] < 1:
                result = False
                break
        return result

    def drawGrid(self, amount=-1):
        height = max([k[1] for k in self.grid])
        amount = height if amount == -1 else amount
        for h in range(height, height - amount, -1):
            line = ""
            for i in range(7):
                line += "#" if (i, h) in self.grid else "."
            print(line)

    def clearUnderFullLine(self, h):
        for k in [k for k in self.grid if k[1] < h]:
            self.grid.pop(k,"")

    def dropBlocks(self, nrOfBlocks):
        self.nrOfBlocks = nrOfBlocks
        for i in range(self.nrOfBlocks):

            self.shapeIdx = (self.shapeIdx+1) % len(self.shapes)
            shape = self.shapes[self.shapeIdx]

            shape = moveShape(shape, 2, 0)
            # Already perform 4 pushes and 3 drops
            # startdirs = "".join([directions[(self.directionIdx+j) % len(directions)] for j in range(3)])

            # self.directionIdx += (self.directionIdx + 3) % len(directions)

            # shape = moveSideways4Times(startdirs, tuple(shape))
            shape = moveShape(shape, 0, self.height + 4)
            fixed = False
            self.switch = 1  # 1 = push, -1 is fall
            while not fixed:
                if self.switch == 1:
                    direction = directions[self.directionIdx%len(directions)]
                    self.directionIdx += 1
                    if direction == ">":
                        newshape = moveShape(shape, 1, 0)
                    else:
                        newshape = moveShape(shape, -1, 0)
                    check = self.checkSidewaysPush(newshape)
                    if check:
                        shape = newshape
                elif self.switch == -1:
                    newshape = moveShape(shape,0,-1)
                    check = self.checkDownwards(newshape)
                    if check:
                        shape = newshape
                    else:
                        fixed = True
                self.switch *= -1

            for pt in shape:
                self.grid[pt] = "#"
            self.height = max(max([k[1] for k in shape]), self.height)
            changedHeights = [k[1] for k in shape]
            for h in changedHeights:
                blocks = 0
                for x in range(7):
                    blocks += 1 if (x,h) in self.grid else 0
                if blocks == 7:
                    self.clearUnderFullLine(h)


        return self.height

